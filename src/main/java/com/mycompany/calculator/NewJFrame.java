    private void proEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_proEqualActionPerformed
         // Retrieve the second number input
    String input2 = jTextField4.getText();  // Second number

    // Convert the second input to decimal
    double decimalValue2 = convertToDecimal(input2);  // Adjusted to double

    double result; // Variable to store the result

    // Check the operation and perform the corresponding calculation
    switch (proOp) {
        case "+":
            result = EnterProNumber1 + decimalValue2;  // Addition
            break;
        case "-":
            result = EnterProNumber1 - decimalValue2;  // Subtraction
            break;
        case "*":
            result = EnterProNumber1 * decimalValue2;  // Multiplication
            break;
        case "/":
            if (decimalValue2 != 0) {  // Division by zero check
                result = EnterProNumber1 / decimalValue2;  // Division
            } else {
                jTextField4.setText("Error: Division by zero");
                return; // Stop further execution if division by zero
            }
            break;
        default:
            jTextField4.setText("Error: Invalid operation");
            return;  // Stop execution if the operation is invalid
    }

    // Display the result in jTextField4
    jTextField4.setText(String.valueOf(result));

    }//GEN-LAST:event_proEqualActionPerformed

    
    

    private int convertToDecimal(String input) {
    // Remove leading/trailing spaces
    input = input.trim();

    // Determine the base and convert to decimal
    if (input.matches("[01]+")) { // Binary
        return Integer.parseInt(input, 2);
    } else if (input.matches("[0-7]+")) { // Octal
        return Integer.parseInt(input, 8);
    } else if (input.matches("[0-9A-Fa-f]+")) { // Hexadecimal
        return Integer.parseInt(input, 16);
    } else { // Decimal (default)
        return Integer.parseInt(input); // Assumed to be in decimal format
    }
}
    

