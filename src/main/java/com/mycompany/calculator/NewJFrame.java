    private void equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalActionPerformed
        // TODO add your handling code here:
        EnterNumber2 = Double.parseDouble(jTextField1.getText());
            switch (op) {
        case "+":
            result = EnterNumber1 + EnterNumber2;
           
            break;
        case "-":
            result = EnterNumber1 - EnterNumber2;
            break;
        case "*":
            result = EnterNumber1 * EnterNumber2;
            break;
        case "/":
            if (EnterNumber2 != 0) {
                result = EnterNumber1 / EnterNumber2;
            } else {
                jTextField1.setText("Error: Division by zero");
                return; // Stop further execution if division by zero
            }
            break;
        default:
            jTextField1.setText("Error: Invalid operation");
            return;
    }
            jTextField1.setText(String.valueOf(result));
        
    }//GEN-LAST:event_equalActionPerformed

